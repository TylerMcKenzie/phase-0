def get_grade(ave)
	if ave>=90
		p "A"
	elsif ave>=80
		p "B"
	elsif ave>=70
		p "C"
	elsif ave>=60
		p "D"
	elsif ave<=50
		p "F"
	elsif 
		p "Unable to Calculate"
	end
end
